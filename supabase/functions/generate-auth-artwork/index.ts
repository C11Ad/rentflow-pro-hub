import { serve } from "https://deno.land/std@0.190.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const handler = async (req: Request): Promise<Response> => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const lovableApiKey = Deno.env.get("LOVABLE_API_KEY");
    if (!lovableApiKey) {
      throw new Error("LOVABLE_API_KEY not configured");
    }

    // Generate beautiful Ghanaian real estate themed artwork
    const artworkPrompts = [
      "Beautiful Ghanaian modern apartment building with traditional Kente pattern accents, sunset lighting, architectural photography, vibrant colors, professional real estate photography",
      "Luxury Ghana property with traditional African architectural elements, palm trees, colorful facade with Adinkra symbols, professional photography, warm golden hour lighting",
      "Contemporary Ghanaian residential complex with traditional art murals, lush tropical landscaping, modern design meets African heritage, architectural visualization"
    ];

    const randomPrompt = artworkPrompts[Math.floor(Math.random() * artworkPrompts.length)];

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        "Authorization": `Bearer ${lovableApiKey}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash-image-preview",
        messages: [
          {
            role: "user",
            content: randomPrompt
          }
        ],
        modalities: ["image", "text"]
      })
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI API error:", errorText);
      throw new Error(`Failed to generate image: ${response.status}`);
    }

    const data = await response.json();
    const imageUrl = data.choices?.[0]?.message?.images?.[0]?.image_url?.url;

    if (!imageUrl) {
      throw new Error("No image generated");
    }

    console.log("Successfully generated Ghanaian real estate artwork");

    return new Response(
      JSON.stringify({
        success: true,
        imageUrl,
        description: data.choices?.[0]?.message?.content || "Ghanaian real estate artwork"
      }),
      {
        status: 200,
        headers: {
          "Content-Type": "application/json",
          ...corsHeaders,
        },
      }
    );
  } catch (error: any) {
    console.error("Error generating artwork:", error);
    return new Response(
      JSON.stringify({
        success: false,
        error: error.message,
      }),
      {
        status: 500,
        headers: {
          "Content-Type": "application/json",
          ...corsHeaders,
        },
      }
    );
  }
};

serve(handler);
